namespace MarkForth.Html;

/// <summary>
/// Names of standard HTML elements.
/// </summary>
public static class ElementNames
{
    /// <summary>
    /// The body element represents the contents of the document.
    /// <br/> https://html.spec.whatwg.org/#the-body-element
    /// </summary>
    public const string Body = "body";

    /// <summary>
    /// The head element represents a collection of metadata for the Document.
    /// <br/> https://html.spec.whatwg.org/#the-head-element
    /// </summary>
    public const string Head = "head";

    /// <summary>
    /// The html element represents the root of an HTML document.
    /// <br/> https://html.spec.whatwg.org/#the-html-element
    /// </summary>
    public const string Html = "html";

    /// <summary>
    /// An img element represents an image.
    /// <br/> https://html.spec.whatwg.org/#the-img-element
    /// </summary>
    public const string Img = "img";

    /// <summary>
    /// The pre element represents a block of preformatted text,
    /// in which structure is represented by typographic conventions rather than by elements.
    /// <br/> https://html.spec.whatwg.org/#the-pre-element
    /// </summary>
    public const string Pre = "pre";

    /// <summary>
    /// The script element allows authors to include dynamic script and data blocks in their documents.
    /// <br/> https://html.spec.whatwg.org/#the-script-element
    /// </summary>
    public const string Script = "script";

    /// <summary>
    /// The span element doesn't mean anything on its own,
    /// but can be useful when used together with the global attributes, e.g. class, lang, or dir.
    /// It represents its children.
    /// <br/> https://html.spec.whatwg.org/#the-span-element
    /// </summary>
    public const string Span = "span";

    /// <summary>
    /// The style element allows authors to embed style information in their documents
    /// <br/> https://html.spec.whatwg.org/#the-style-element
    /// </summary>
    public const string Style = "style";

    /// <summary>
    /// The template element is used to declare fragments of HTML that can be cloned and inserted in the document by script.
    /// <br/> https://html.spec.whatwg.org/#the-template-element
    /// </summary>
    public const string Template = "template";
}
