namespace MarkForth.Html;

/// <summary>
/// Names of global HTML attributes.
/// </summary>
public static class AttributeNames
{
    /// <summary>
    /// The id attribute specifies its element's unique identifier (ID).
    /// <br/> https://html.spec.whatwg.org/#the-id-attribute
    /// </summary>
    public const string Id = "id";

    /// <summary>
    /// The src attribute specifies the address of a resource.
    /// <br/> https://html.spec.whatwg.org/#attributes-3:attr-img-src
    /// </summary>
    public const string Src = "src";
}
